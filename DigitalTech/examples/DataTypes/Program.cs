// Basic Data Types

string name = "Joshua";

char gender = 'M';

int age = 17;

double height = 5.6;

bool canCode = false;

// Other Data Types

// Shorts can only hold around 32000
short daysSinceChristmas = 93;

// Use can use underscores like a comma
// 1,000 -> 1_000
long aVeryLargeNumber =  9_223_372_036_854_775_807;

// Like a double, a float can store floating point numbers ( decimals )
// This values should be appended with an 'f' to show that the type is a float
float oneHalf = 1.5f;


// This converts a string to an integer
int convertedNumber = Convert.ToInt32("400");
